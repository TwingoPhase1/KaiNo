/**
 * Custom React hook for dynamic internationalization (i18n).
 * Establishes the current locale using browser settings and server settings:
 * 1. Read 'navigator.language' and matches its 2-letter ISO prefix.
 * 2. If it is one of the supported languages, use it.
 * 3. Otherwise, query '/api/settings' to fetch the default server language.
 * 4. Falls back to 'fr' (French) if settings cannot be fetched.
 */
export function useTranslation() {
  const [lang, setLang] = useState<SupportedLanguage>('fr');
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    let isMounted = true;

    const detectLanguage = async () => {
      // 1. Get browser/telephone language prefix (e.g. 'en-US' -> 'en')
      const rawBrowserLang = typeof navigator !== 'undefined' ? navigator.language : '';
      const browserPrefix = rawBrowserLang.split('-')[0].toLowerCase();

      // 2. Check if the browser language is supported
      if (SUPPORTED_LANGUAGES.includes(browserPrefix as SupportedLanguage)) {
        if (isMounted) {
          setLang(browserPrefix as SupportedLanguage);
          setLoading(false);
        }
        return;
      }

      // 3. If phone language is not supported, retrieve server default language
      try {
        const resp = await fetch('/api/settings');
        if (resp.ok) {
          const data = await resp.json();
          const serverDefault = data.default_language;
          if (SUPPORTED_LANGUAGES.includes(serverDefault as SupportedLanguage)) {
            if (isMounted) {
              setLang(serverDefault as SupportedLanguage);
            }
          }
        }
      } catch (err) {
        console.error('Failed to load server default language, fallback to French:', err);
      } finally {
        if (isMounted) {
          setLoading(false);
        }
      }
    };

    detectLanguage();

    return () => {
      isMounted = false;
    };
  }, []);

  const t = (key: string): string => {
    const translationSet = translations[lang] || translations['fr'];
    return translationSet[key] || key;
  };

  return { t, lang, setLang, loadingTranslations: loading };
}
